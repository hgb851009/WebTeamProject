package domain;

public class AvatarVO {
	private String avatarId;
	private int avatarBody;
	private int avatarHair;
	private int avatarShirt;
	
	
	public String getAvatarId() {
		return avatarId;
	}
	public void setAvatarId(String avatarId) {
		this.avatarId = avatarId;
	}
	public int getAvatarBody() {
		return avatarBody;
	}
	public void setAvatarBody(int avatarBody) {
		this.avatarBody = avatarBody;
	}
	public int getAvatarHair() {
		return avatarHair;
	}
	public void setAvatarHair(int avatarHair) {
		this.avatarHair = avatarHair;
	}
	public int getAvatarShirt() {
		return avatarShirt;
	}
	public void setAvatarShirt(int avatarShirt) {
		this.avatarShirt = avatarShirt;
	}
}
